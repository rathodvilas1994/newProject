package conditionalsmt;      //find the greater number btn 2 numbers using ternary operator

public class GreaterTwoNumers2 {

	public static void main(String[] args) {
		
		int no1=100;
		int no2=20;
		
		int res=(no1>no2)? no1:no2;                    //ternary operator= (condition)?(expression1):(expression2)
		System.out.println(  res+  "  is greater");

	}

}
